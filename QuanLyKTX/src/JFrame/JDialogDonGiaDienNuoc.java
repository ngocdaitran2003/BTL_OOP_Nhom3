    void showDichVu() {
        Connection conn = KetNoiSQL.getConnection();
        String sql = "select * from dichvu where ID = 1";
        try {
            PreparedStatement preparedStatement = conn.prepareStatement(sql);
            ResultSet rs = preparedStatement.executeQuery();

            while (rs.next()) {
                txtDonGiaDien.setText(String.valueOf(rs.getString("tienDien")));
                txtDonGiaNuoc.setText(String.valueOf(rs.getString("tienNuoc")));
                txtDonGiaMang.setText(String.valueOf(rs.getString("tienMang")));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void LuuDichVu() {
        String ycso = "^[0-9.E]*[0-9]";
        String dongianuoc = txtDonGiaNuoc.getText().trim();
        String dongiadien = txtDonGiaDien.getText().trim();
        String dongiamang = txtDonGiaMang.getText().trim();

        if (!dongianuoc.matches(ycso)) {
            JOptionPane.showMessageDialog(this, "Định dạng số nước sai. Vui lòng nhập lại!");
        } else if (!dongiadien.matches(ycso)) {
            JOptionPane.showMessageDialog(this, "Định dạng số nước sai. Vui lòng nhập lại!");
        } else if (!dongiamang.matches(ycso)) {
            JOptionPane.showMessageDialog(this, "Định dạng số nước sai. Vui lòng nhập lại!");
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn lưu đơn giá hay không ?", "Thông báo", JOptionPane.YES_NO_OPTION);

            if (choice == JOptionPane.YES_OPTION) {
                float tiendien = Float.valueOf(dongiadien);
                float tiennuoc = Float.valueOf(dongianuoc);
                float tienmang = Float.valueOf(dongiamang);
                Connection conn = KetNoiSQL.getConnection();
                String sql = "update dichvu set tienDien = ?, tienNuoc =?, tienMang = ? where ID =1";
                try {

                    PreparedStatement ps = conn.prepareStatement(sql);

                    ps.setFloat(1, tiendien);
                    ps.setFloat(2, tiennuoc);
                    ps.setFloat(3, tienmang);

                    ps.executeUpdate();
                    conn.close();
                    JOptionPane.showMessageDialog(this, "Cập nhật đơn giá thành công", "Thông báo", JOptionPane.CANCEL_OPTION);

                } catch (Exception e) {
                }
            }

        }
    }
