    private void LocKetQuaAll(String tungay, String denngay) {
        Locale localeVN = new Locale("vi", "VN");

        NumberFormat currencyVN = NumberFormat.getCurrencyInstance(localeVN);
        Byte trangthai = 1;
        listhdp = new HoaDonPhongDAO().searchHoaDonPhongAll(tungay, denngay, trangthai);
        listhdsv = new ThongTinHoaDonDAO().searchHoaDonSVAll(tungay, denngay, trangthai);
        trangthai = 0;
        listhdpno = new HoaDonPhongDAO().searchHoaDonPhongAll(tungay, denngay, trangthai);
        listhdsvno = new ThongTinHoaDonDAO().searchHoaDonSVAll(tungay, denngay, trangthai);
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat formatter1 = new SimpleDateFormat("MM/yyyy");
        double tiendichvu = 0;
        double tiendichvuno = 0;
        double tienphong = 0;
        double tienphongno = 0;
        int hddvno = 0;
        int hdtienphongno = 0;

        for (HoaDonPhong hdp : listhdp) {
            tiendichvu += hdp.getTongTien();
        }
        for (HoaDonPhong hdp : listhdpno) {
            tiendichvuno += hdp.getTongTien();
            hddvno += 1;
        }
        for (ThongTinHoaDonSV hdsv : listhdsv) {
            tienphong += hdsv.getTienThanhToan();
        }
        for (ThongTinHoaDonSV hdsv : listhdsvno) {
            tienphongno += hdsv.getTienThanhToan();
            hdtienphongno += 1;
        }

        txtTongDoanhThu.setText(currencyVN.format(tiendichvu + tienphong));
        txtTienPhong.setText(currencyVN.format(tienphong));
        txtTienDichVu.setText(currencyVN.format(tiendichvu));
        txtTienDVNo.setText(currencyVN.format(tiendichvuno));
        txtTienPhongNo.setText(currencyVN.format(tienphongno));
        txtSoHoaDonDVNo.setText(String.valueOf(hddvno));
        txtSoHoaDonPhongNo.setText(String.valueOf(hdtienphongno));

    }

