    void setBieuDoDichVu() {
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        String tungay = formatter.format(LayNgayThangTuBieuDo());

        chart.clear();
        List<ThongTinPhong> listphong = new PhongDAO().getAllThongTinPhong();
        for (ThongTinPhong phong : listphong) {
            double tiendichvu = 0;
            tiendichvu = new ThongTinDichVuDAO().SoTienDichVuTheoPhong(phong.getMaPhong(), tungay);

            chart.addData(new ModelChart(phong.getMaPhong(), new double[]{tiendichvu}));
        }
        chart.start();
    }

