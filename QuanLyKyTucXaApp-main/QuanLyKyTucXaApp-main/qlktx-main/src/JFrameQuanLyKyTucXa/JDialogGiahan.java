public void addGiaHan(String maSV){
        int soThangGH =0;
        if (cbGiaHan.getSelectedItem()=="3 tháng") {
            soThangGH=3;
        }else if (cbGiaHan.getSelectedItem()=="6 tháng") {
            soThangGH=6;
        }else if (cbGiaHan.getSelectedItem()=="9 tháng") {
            soThangGH=9;
        }else  soThangGH=12;
        
        
        Connection conn = KetNoiSQL.getConnection();
        ThongTinHopDong hd = new HopDongKTXDAO().getAllThongTinHDTheoMaSV(msv);
        String sql = "insert into GiaHan (maSV,ngayKTHD,thoiGianGH,trangThai)"
                + "values(?,?,?,?)";
        int row = 0;
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, msv);
            ps.setDate(2, (java.sql.Date) hd.getNgayHDKT());
            ps.setInt(3, soThangGH );  
            ps.setString(4, "1" ); 
            row = ps.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Nhap ma chua dung");
            }
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }


    } 
    
