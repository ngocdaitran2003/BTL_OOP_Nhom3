    public void updateHDKTKTX(String masv, int thang, Date ngayHDBD) {
        Date datesv;
        String datestr = "";
        SimpleDateFormat sp = new SimpleDateFormat("yyyy-MM-dd");
        Calendar cld = Calendar.getInstance();
        Date date1 = DateVao.getDate();

        cld.setTime(date1);
        cld.add(Calendar.MONTH, thang);
        datesv = cld.getTime();
        datestr = sp.format(datesv);
        Connection con = KetNoiSQL.getConnection();
        String sql = "Update DangKyPhong set ngayHDKT=?,ngayHDBD=? where maSV=?";

        try {

            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, datestr); // Convert to java.sql.Date
            ps.setDate(2, new java.sql.Date(ngayHDBD.getTime()));
            ps.setString(3, masv);
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(DangKyPhong.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

