    public void addChuyenPhong(String maSV) {
        Connection conn = KetNoiSQL.getConnection();
        ThongTinHopDong hd = new HopDongKTXDAO().getAllThongTinHDTheoMaSV(maSV);
        String sql = "insert into ChuyenPhong (maSV,maPhongHT,maPhongChuyen,lyDoChuyen,trangThai)"
                + "values(?,?,?,?,?)";
        int row = 0;
        try {
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, hd.getMasv().trim());
            ps.setString(2, hd.getMaphong().trim());
            ps.setString(3, (String) txtMaPM.getSelectedItem());
            ps.setString(4, txtLiDo.getText().trim());
            ps.setString(5, "1");
            row = ps.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Nhap ma chua dung");
            }
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

    void AddComboBoxnew() {
        txtMaPM.removeAllItems();
        PhongDAO pd = new PhongDAO();
        setGT();
        List<ThongTinPhong> listphong = new PhongDAO().getAllThongTinPhongTheoGT_SL(gioiTinh);
        for (ThongTinPhong phong : listphong) {
            txtMaPM.addItem(phong.getMaPhong());
            txtMaPM.removeItem(layMP());
        }
    }

