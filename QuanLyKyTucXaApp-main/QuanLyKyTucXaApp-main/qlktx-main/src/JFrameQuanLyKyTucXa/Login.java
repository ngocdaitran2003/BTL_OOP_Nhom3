    private void btnloginupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginupActionPerformed
        // TODO add your handling code here:
        String un = jnameuser.getText().trim();
        String ps = jpassword.getText().trim();

        if (un.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên đăng nhập!");
        } else if (ps.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu!");
        } else {
            try {
                Connection conn = KetNoiSQL.getConnection();

                Statement stm = conn.createStatement();

                String sql = "select * from TaiKhoan where tenDangNhap='" + un + "' and matKhau='" + ps + "'";
                ResultSet rs = stm.executeQuery(sql);

                if (rs.next()) {
                    if (rs.getString("phanQuyen").equals("Quản lý")) {
                        this.email = new TaiKhoanDAO().LayEmail(un);
                        ThongTinNhanVien nv = new QuanLyNhanVienDAO().getAllThongTinNVTheoEmail(email);
                        if (nv.getTrangThai().equals("Đã nghỉ việc")) {
                            JOptionPane.showMessageDialog(null, "Bạn đã nghĩ việc ! Không thể đăng nhập đc!");
                        } else {
                            dispose();
                            Main main = new Main();
                            main.setVisible(true);
                            this.email = new TaiKhoanDAO().LayEmail(un);
                            main.setnameuser(email);
                        }
                    } else if (rs.getString("phanQuyen").equals("Sinh Viên")) {
                        dispose();
                        Main_USER main = new Main_USER();
                        main.setVisible(true);
                        this.email = new TaiKhoanDAO().LayEmail(un);
                        main.setEmail(email);

                        //   main.setnameuser(un);
                    } else if (rs.getString("phanQuyen").equals("Nhân viên")) {
                        this.email = new TaiKhoanDAO().LayEmail(un);
                        ThongTinNhanVien nv = new QuanLyNhanVienDAO().getAllThongTinNVTheoEmail(email);
                        if (nv.getTrangThai().equals("Đã nghỉ việc")) {
                            JOptionPane.showMessageDialog(null, "Bạn đã nghĩ việc ! Không thể đăng nhập đc!");
                        } else {
                            dispose();
                            Main_NV main = new Main_NV();
                            main.setVisible(true);
                            main.setnameuser(email);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Tên đăng nhập hoặc mật khẩu không đúng!");
                    jnameuser.setText("");
                    jpassword.setText("");
                }

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }

        }
    }//GEN-LAST:event_btnloginupActionPerformed

