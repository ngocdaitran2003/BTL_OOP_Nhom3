    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        if (new GiaHanDAO().KiemTraMaSV(maSV)) {
            JDialogKQGiahan gh = new JDialogKQGiahan(new javax.swing.JFrame(), true);
            gh.setLocationRelativeTo(null);
            gh.setMSV(maSV);
            gh.LayThongTinHDUp(maSV);
            gh.setVisible(true);
            gh.deleteChuyenPhong(maSV);
        }else{
            JDialogGiahan gh = new JDialogGiahan(new javax.swing.JFrame(), true);
            gh.setLocationRelativeTo(null);
            gh.setMSV(maSV);
            gh.LayThongTinHDUp(maSV);
            gh.setVisible(true);
        }

    }//GEN-LAST:event_jButton3MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if (new ChuyenPhongDAO().KiemTraMaSV(maSV)) {
            JDialogKQChuyenPhong chuyen = new JDialogKQChuyenPhong(new javax.swing.JFrame(), true);
            chuyen.setLocationRelativeTo(null);
            chuyen.setMaSV(maSV);
            chuyen.LayThongTinChuyenPhongUp(maSV);
            chuyen.setVisible(true);
            chuyen.deleteChuyenPhong(maSV);
            
        }else{
            JDialogChuyenPhong chuyen = new JDialogChuyenPhong(new javax.swing.JFrame(), true);
            chuyen.setLocationRelativeTo(null);
            chuyen.setMaSV(maSV);
            chuyen.LayThongTinUp();
            chuyen.setVisible(true);
            chuyen.AddComboBoxnew();
        }

    }//GEN-LAST:event_jButton2MouseClicked

